package model;

public class myPoint {
    private int x;  //x point
    private int y; //y point

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
    public void setPoint(int x, int y){
        this.x = x;
        this.y = y;
    }
}
